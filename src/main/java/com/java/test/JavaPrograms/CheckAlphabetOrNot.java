package com.java.test.JavaPrograms;

public class CheckAlphabetOrNot {
    public static void main(String[] args) {

        char c = 'S';

        if(c >= 'A' && c <='Z' ){
            System.out.println(c + " is an alphabet");
        }else {
            System.out.println(c + " is not an alphabet");
        }
    }
}
